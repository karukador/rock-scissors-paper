import pyttsx3 # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å –¥–ª—è –æ–∑–≤—É—á–∏–≤–∞–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ –≤ —Ä–µ—á—å
from random import randint

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –¥–≤–∏–∂–æ–∫ –¥–ª—è –æ–∑–≤—É—á–∏–≤–∞–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ –≤ —Ä–µ—á—å
engine = pyttsx3.init()

# –°–ª–æ–≤–∞—Ä—å –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –∑–Ω–∞—á–∫–æ–≤
menu = {1: "üóø", 2: "‚úÇÔ∏è", 3: "üìÑ", 4: "–í—ã—Ö–æ–¥"}

# –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è –≤—ã–±–æ—Ä–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ —Å—á–µ—Ç–∞
choice = 0
user_score = 0
computer_score = 0


# –§—É–Ω–∫—Ü–∏—è –≤—ã–≤–æ–¥–∞ –º–µ–Ω—é
def show_menu():
    print("–í—ã–±–µ—Ä–∏ –¥–µ–π—Å—Ç–≤–∏–µ, –≤–≤–µ–¥—è —á–∏—Å–ª–æ –∏–∑ —Å–ø–∏—Å–∫–∞ –Ω–∏–∂–µ")
    for choice in range(1, 5):
        print(f"{choice}: {menu[choice]}")


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –ø–æ–±–µ–¥–∏—Ç–µ–ª—è —Ä–∞—É–Ω–¥–∞
def who_wins(user, computer):
    # –û–¥–∏–Ω–∞–∫–æ–≤—ã–π –≤—ã–±–æ—Ä - –Ω–∏—á—å—è –≤ —Ä–∞—É–Ω–¥–µ
    if user == computer:
        return "–ù–∏—á—å—è"
    # –ù–∞–±–æ—Ä —É—Å–ª–æ–≤–∏–π, –ø—Ä–∏ –∫–æ—Ç–æ—Ä—ã—Ö –ø–æ–±–µ–∂–¥–∞–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
    elif (user == 1 and computer == 2) or (user == 2 and computer == 3) or (user == 3 and computer == 1):
        return "–¢—ã"
    # –õ—é–±–∞—è –¥—Ä—É–≥–∞—è —Å–∏—Ç—É–∞—Ü–∏—è - –ø–æ–±–µ–¥–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä–∞
    else:
        return "–ö–æ–º–ø—å—é—Ç–µ—Ä"


# –§—É–Ω–∫—Ü–∏—è —Å–ª–æ–≤–µ—Å–Ω–æ–≥–æ –æ–ø–∏—Å–∞–Ω–∏—è —Ç–µ–∫—É—â–µ–≥–æ –∏—Å—Ö–æ–¥–∞
def score_characteristic(user, computer):
    if user > computer:
        return "–≤ —Ç–≤–æ—é –ø–æ–ª—å–∑—É"
    elif computer > user:
        return "–≤ –ø–æ–ª—å–∑—É –∫–æ–º–ø—å—é—Ç–µ—Ä–∞"
    else:
        return ". –ü–æ–∫–∞ –Ω–∏—á—å—è"


# –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–≥–æ –≤–≤–æ–¥–∞
def input_and_check(current_choice):
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –≤–≤–æ–¥–∞ (–∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é choice –º—ã –ø–æ–ª–æ–∂–∏–ª–∏ 0)
    while not (1 <= current_choice <= 4):
        show_menu()
        user_input = input()
        if user_input.isdigit() and 1 <= int(user_input) <= 4:
            current_choice = int(user_input)

    return current_choice


# –ë–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª, –∫–∞–∂–¥–∞—è –∏—Ç–µ—Ä–∞—Ü–∏—è - –Ω–æ–≤—ã–π —Ä–∞—É–Ω–¥
# –ü—Ä–µ—Ä–≤–µ–º –µ–≥–æ, –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±–µ—Ä–µ—Ç "–í—ã—Ö–æ–¥"
while True:
    choice = input_and_check(choice)
    if choice == 4:
        # –í—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–æ–≤—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏–≥—Ä—ã
        if user_score <= computer_score:
            print("–ò—Ç–æ–≥:\n–ö–æ–º–ø—å—é—Ç–µ—Ä –≤—ã–∏–≥—Ä–∞–ª! –ë—ã–ª–æ –∫—Ä—É—Ç–æ!")
            engine.say("–ò—Ç–æ–≥: –ö–æ–º–ø—å—é—Ç–µ—Ä –≤—ã–∏–≥—Ä–∞–ª! –ë—ã–ª–æ –∫—Ä—É—Ç–æ!")
        elif user_score >= computer_score:
            print("–ò—Ç–æ–≥:\n–¢—ã –≤—ã–∏–≥—Ä–∞–ª! –ë—ã–ª–æ –∫—Ä—É—Ç–æ!")
            engine.say("–ò—Ç–æ–≥: –¢—ã –≤—ã–∏–≥—Ä–∞–ª! –ë—ã–ª–æ –∫—Ä—É—Ç–æ!")
        else:
            print("–ò—Ç–æ–≥:\n–ù–∏—á—å—è! –ë—ã–ª–æ –∫—Ä—É—Ç–æ!")
            engine.say("–ò—Ç–æ–≥: –ù–∏—á—å—è! –ë—ã–ª–æ –∫—Ä—É—Ç–æ!")

        # –û–∑–≤—É—á–∏–≤–∞–µ–º –∏—Ç–æ–≥–æ–≤—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏–≥—Ä—ã
        engine.runAndWait()

        break

    # –°–ª—É—á–∞–π–Ω—ã–π –≤—ã–±–æ—Ä –∫–æ–º–ø—å—é—Ç–µ—Ä–∞ –æ—Ç 1 –¥–æ 3 –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ –±—É–¥–µ—Ç –µ–≥–æ —Ö–æ–¥–æ–º
    computer_choice = randint(1, 3)

    # –ü–µ—Ä–µ–¥–∞–µ–º –≤ —Å–≤–æ—é —Ñ—É–Ω–∫—Ü–∏—é –≤—ã–±–æ—Ä –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –∫–æ–º–ø—å—é—Ç–µ—Ä–∞, —Å–æ—Ö—Ä–∞–Ω—è–µ–º –≤–æ–∑–≤—Ä–∞—â–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
    winner = who_wins(choice, computer_choice)

    # –í –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ç–æ–≥–æ, –∫—Ç–æ –≤—ã–∏–≥—Ä–∞–ª, —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á–µ—Ç
    if winner == "–¢—ã":
        user_score += 1
    elif winner == "–ö–æ–º–ø—å—é—Ç–µ—Ä":
        computer_score += 1

    # –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –∏—Å—Ö–æ–¥–æ–º —Ç–µ–∫—É—â–µ–≥–æ —Ä–∞—É–Ω–¥–∞ –∏ –æ–±—â–∏–º —Å—á–µ—Ç–æ–º
    print(
        f"–¢—ã - {menu[choice]}, –∫–æ–º–ø—å—é—Ç–µ—Ä - {menu[computer_choice]}. \n–ü–æ–±–µ–¥–∏—Ç–µ–ª—å —Ä–∞—É–Ω–¥–∞: {winner}. \n–°—á–µ—Ç "
        f"{user_score}:{computer_score} {score_characteristic(user_score, computer_score)}.\n")

    # –ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ –ø—Ä–æ–∏–∑–Ω–æ—à–µ–Ω–∏–µ –∏–∫–æ–Ω–æ–∫
    if menu[choice] == "üóø":
        icon_u = "–ö–∞–º–µ–Ω—å"
    if menu[choice] == "‚úÇÔ∏è":
        icon_u = "–ù–æ–∂–Ω–∏—Ü—ã"
    if menu[choice] == "üìÑ":
        icon_u = "–ë—É–º–∞–≥–∞"
    if menu[computer_choice] == "üóø":
        icon_c = "–ö–∞–º–µ–Ω—å"
    if menu[computer_choice] == "‚úÇÔ∏è":
        icon_c = "–ù–æ–∂–Ω–∏—Ü—ã"
    if menu[computer_choice] == "üìÑ":
        icon_c = "–ë—É–º–∞–≥–∞"

    # –û–∑–≤—É—á–∏–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Ç–µ–∫—É—â–µ–≥–æ —Ä–∞—É–Ω–¥–∞
    engine.say(f"–¢—ã - {icon_u}, –∫–æ–º–ø—å—é—Ç–µ—Ä - {icon_c}. –ü–æ–±–µ–¥–∏—Ç–µ–ª—å —Ä–∞—É–Ω–¥–∞: {winner}. –°—á—ë—Ç: "
               f"{user_score} {computer_score}")
    engine.runAndWait()

    # –û–±–Ω—É–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π –≤—ã–±–æ—Ä, —á—Ç–æ–±—ã –∑–∞–Ω–æ–≤–æ –µ–≥–æ –∑–∞–ø—Ä–æ—Å–∏—Ç—å –Ω–∞ —Å–ª–µ–¥—É—é—â–µ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏
    choice = 0
